# 现有字符串： str1 = '1234567890'，根据题目要求，将截取后的新字符串赋值给str2
str1 = '1234567890'
# 截取字符串的第一位到第三位的字符
print(str1[0:3])
# 截取字符串最后三位的字符
print(str1[-3:])
# 截取字符串的全部字符
print(str1[:])
# 截取字符串的第七个字符到结尾
print(str1[6:])
# 截取字符串的第一位到倒数第三位之间的字符
print(str1[0:-2])
# 截取字符串的第三个字符
print(str1[2])
# 截取字符串的倒数第一个字符
print(str1[-1])
# 截取与原字符串顺序相反的字符串
print(str1[::-1])

